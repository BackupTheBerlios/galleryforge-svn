	<h3>Introduction</h3>
	
	<p>If you just happen to be an enthusiastic photographer who likes to publish his photos online, you may realize that it would be nice to cater to your visitors a bit and organize your collection in some way, perhaps adopting the standard convention of sorting your photos into <i>albums</i>, which display <i>thumbnails</i>. Voil√°, galleryforge happens to do just that.
	</p>
	
	<p>All you need to do is sort your photos into directories and galleryforge will rummage through them all, resize images, create thumbnails, write index pages, the whole shebang. But the greatest benefit lies not in creating your albums for the first time, it is that you can continue to add directories, and galleryforge will update your collection everytime, without touching what is already there.
	</p>
	
	<p>Want to see a <a href="demo">demo</a> of a gallery created by galleryforge?
	</p>
	
	<h3>Features</h3>
	<ul>
		<li>Finds images recursively in a hierarchy of directories.</li>
		<li>Does not overwrite existing galleries.</li>
		<li>Highly customizable templates for all pages.</li>
		<li>Changes in templates are applied to existing pages.</li>
		<li>Generates XHTML &amp; CSS code to standard.</li>
		<li>Platform independent implementation in <a href="http://www.python.org">Python</a> and <a href="http://www.wxwidgets.org/">wxWidgets</a>.</li>
	</ul>
	
